let day = 10;

switch (day) {
  case 1:
    console.log("it's Sunday");
    break;
  case 2:
    console.log("it's Monday");
    break;
  case 3:
    console.log("it's Tuesday");
    break;
  case 4:
    console.log("it's Wendesday");
    break;
  case 5:
    console.log("it's Thursday");
    break;
  case 6:
    console.log("it's Friday");
    break;
  case 7:
    console.log("it's Saturday");
    break;
  default:
    console.log(`${day} not a day`);
    break;
}
